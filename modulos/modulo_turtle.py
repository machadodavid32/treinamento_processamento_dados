from turtle import Turtle

# Módulo para fazer gráficos

# Iniciar uma turtle
t = Turtle()

t.speed(1) # velocidade de movimentação

t.forward(100) # vai 100 pixels para frente

t.right(90) # rotacionar em 90 graus para direita

t.forward(100)

t.left(90) # rotacionar em 90 graus para esquerda

t.forward(100)

t.backward(200) # Movimentar para tras 200 pixels

input()